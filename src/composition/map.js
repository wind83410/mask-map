export const mapInit = () => {
  onMounted(() => {
    map.value = L.map('map', {
      center: [22.604799,120.2976256],
      zoom: 8
    });

    L.tileLayer('https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png', {
      attribution: '&copy; <a href="https://www.openstreetmap.org/copyright">OpenStreetMap</a> contributors'
    }).addTo(map.value);
  })
}